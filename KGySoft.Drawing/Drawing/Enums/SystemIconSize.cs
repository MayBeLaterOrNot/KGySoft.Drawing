namespace KGySoft.Drawing
{
    /// <summary>
    /// Represents predefined system icon sizes (16x16 and 32x32 sizes only).
    /// </summary>
    public enum SystemIconSize: uint
    {
        /// <summary>
        /// Represents the 32x32 system icon size.
        /// </summary>
        Large = 0x0,

        /// <summary>
        /// Represents the 16x16 system icon size.
        /// </summary>
        Small = 0x1
    }
}